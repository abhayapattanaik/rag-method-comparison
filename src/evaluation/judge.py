"""LLM-as-Judge scoring for the RAG Comparison System.

Scores RAG pipeline answers on 4 metrics using hand-crafted prompt templates.
Each metric call makes one LLM call and returns a JudgeResult with score,
justification, and the raw LLMResponse.

Metrics:
    CONTEXT_PRECISION  -- proportion of retrieved chunks that are relevant to the question
    CONTEXT_RECALL     -- proportion of ground truth covered by retrieved chunks
    FAITHFULNESS       -- proportion of answer claims supported by retrieved chunks
    ANSWER_RELEVANCY   -- how directly the answer addresses the question

Telemetry operation labels: "judge_context_precision", "judge_context_recall",
    "judge_faithfulness", "judge_answer_relevancy"

Typical usage:
    judge = Judge(provider, config, telemetry)
    results = judge.score_all_metrics(
        question="What is RAG?",
        answer="RAG stands for ...",
        contexts=["chunk1 ...", "chunk2 ..."],
        ground_truth="RAG is a technique that ...",
    )
"""

from __future__ import annotations

import json
import logging
import time
from dataclasses import dataclass
from enum import Enum
from typing import TYPE_CHECKING

if TYPE_CHECKING:
    from src.config import AppConfig
    from src.llm.base import BaseLLMProvider, LLMResponse
    from src.telemetry import TelemetryTracker

logger = logging.getLogger(__name__)


# ---------------------------------------------------------------------------
# MetricType enum
# ---------------------------------------------------------------------------


class MetricType(Enum):
    CONTEXT_PRECISION = "context_precision"
    CONTEXT_RECALL = "context_recall"
    FAITHFULNESS = "faithfulness"
    ANSWER_RELEVANCY = "answer_relevancy"


# ---------------------------------------------------------------------------
# JudgeResult dataclass
# ---------------------------------------------------------------------------


@dataclass
class JudgeResult:
    """Result of scoring one metric for one answer."""

    metric: MetricType
    score: float           # 0.0 to 1.0
    justification: str
    llm_response: "LLMResponse"

    @property
    def input_tokens(self) -> int:
        """Prompt tokens consumed by this judge LLM call."""
        return self.llm_response.input_tokens

    @property
    def output_tokens(self) -> int:
        """Completion tokens generated by this judge LLM call."""
        return self.llm_response.output_tokens


# ---------------------------------------------------------------------------
# Prompt templates (one per metric)
# ---------------------------------------------------------------------------

_SYSTEM_PROMPT = (
    "You are an objective evaluation judge for RAG (Retrieval-Augmented Generation) systems. "
    "You score answers strictly based on provided criteria. "
    "You always respond with valid JSON only — no prose, no markdown fences."
)

# Each template receives: {question}, {answer}, {contexts_block}, {ground_truth}

_CONTEXT_PRECISION_PROMPT = """\
## Task: Evaluate Context Precision

**Definition:** Context Precision measures what proportion of the retrieved context chunks \
are actually relevant to answering the given question. A score of 1.0 means all retrieved \
chunks are relevant; 0.0 means none are relevant.

**Scoring criteria:**
- Count how many of the retrieved chunks contain information useful for answering the question.
- Divide by the total number of retrieved chunks.
- A chunk is relevant if it directly contains information needed to answer the question.
- A chunk is irrelevant if it is about a different topic, even if from the same source.

**Question:**
{question}

**Retrieved Context Chunks:**
{contexts_block}

**Ground Truth Answer (for reference):**
{ground_truth}

**Instructions:**
Evaluate each retrieved chunk against the question. Determine whether each chunk is relevant \
(contributes useful information for answering this specific question) or irrelevant.

Respond ONLY with a JSON object. No markdown. No prose. No explanation outside the JSON.
{{"score": <float 0.0-1.0>, "justification": "<1-3 sentences explaining your score>"}}"""


_CONTEXT_RECALL_PROMPT = """\
## Task: Evaluate Context Recall

**Definition:** Context Recall measures how much of the ground truth answer is supported by \
the retrieved context chunks. A score of 1.0 means every claim in the ground truth is covered \
by at least one retrieved chunk; 0.0 means none of the ground truth is supported.

**Scoring criteria:**
- Identify the key claims or facts in the ground truth answer.
- For each claim, determine whether at least one retrieved chunk contains supporting evidence.
- Score = (number of claims supported by chunks) / (total number of claims in ground truth).

**Question:**
{question}

**Retrieved Context Chunks:**
{contexts_block}

**Ground Truth Answer:**
{ground_truth}

**Instructions:**
Break the ground truth into individual claims. Check each claim against the retrieved chunks. \
Calculate the proportion supported.

Respond ONLY with a JSON object. No markdown. No prose. No explanation outside the JSON.
{{"score": <float 0.0-1.0>, "justification": "<1-3 sentences explaining your score>"}}"""


_FAITHFULNESS_PROMPT = """\
## Task: Evaluate Faithfulness

**Definition:** Faithfulness measures whether the generated answer is grounded in the retrieved \
context chunks. A score of 1.0 means every claim in the answer is supported by at least one \
retrieved chunk; 0.0 means the answer contains no claims supported by the chunks (hallucination).

**Scoring criteria:**
- Identify the specific claims, facts, or assertions made in the answer.
- For each claim, check whether it is supported by at least one retrieved chunk.
- Do NOT penalise for omissions — only score the claims that ARE made.
- Score = (claims supported by chunks) / (total claims in answer).

**Question:**
{question}

**Generated Answer:**
{answer}

**Retrieved Context Chunks:**
{contexts_block}

**Instructions:**
List the claims made in the answer. Check each against the chunks. Calculate the proportion \
that are grounded in the retrieved context.

Respond ONLY with a JSON object. No markdown. No prose. No explanation outside the JSON.
{{"score": <float 0.0-1.0>, "justification": "<1-3 sentences explaining your score>"}}"""


_ANSWER_RELEVANCY_PROMPT = """\
## Task: Evaluate Answer Relevancy

**Definition:** Answer Relevancy measures how directly and completely the generated answer \
addresses the question that was asked. A score of 1.0 means the answer directly addresses \
the question with no irrelevant content; 0.0 means the answer is completely off-topic or \
refuses to answer.

**Scoring criteria:**
- Does the answer directly respond to what was asked?
- Is the answer focused, or does it include excessive off-topic content?
- Does the answer actually provide the requested information?
- Partial credit for answers that address the question but incompletely or tangentially.
- Do NOT penalise for factual errors — only judge relevance to the question.

**Question:**
{question}

**Generated Answer:**
{answer}

**Ground Truth Answer (for reference — what a complete answer looks like):**
{ground_truth}

**Instructions:**
Evaluate whether and how well the generated answer addresses the question asked. \
Consider directness, completeness, and focus.

Respond ONLY with a JSON object. No markdown. No prose. No explanation outside the JSON.
{{"score": <float 0.0-1.0>, "justification": "<1-3 sentences explaining your score>"}}"""


_METRIC_PROMPTS: dict[MetricType, str] = {
    MetricType.CONTEXT_PRECISION: _CONTEXT_PRECISION_PROMPT,
    MetricType.CONTEXT_RECALL: _CONTEXT_RECALL_PROMPT,
    MetricType.FAITHFULNESS: _FAITHFULNESS_PROMPT,
    MetricType.ANSWER_RELEVANCY: _ANSWER_RELEVANCY_PROMPT,
}


# ---------------------------------------------------------------------------
# Judge class
# ---------------------------------------------------------------------------


class Judge:
    """LLM-as-judge that scores RAG answers on 4 evaluation metrics.

    Args:
        provider:  LLM provider used for all judge calls.
        config:    AppConfig (uses config.evaluation.judge_temperature and
                   config.models.judge_model via the provider).
        telemetry: Optional TelemetryTracker. When provided, each judge call
                   is recorded with operation="judge_{metric_name}".
    """

    def __init__(
        self,
        provider: "BaseLLMProvider",
        config: "AppConfig",
        telemetry: "TelemetryTracker | None" = None,
    ) -> None:
        self._provider = provider
        self._config = config
        self._telemetry = telemetry
        logger.info(
            "Judge initialised: provider=%s model=%s judge_temperature=%.1f",
            provider.__class__.__name__,
            provider.get_model_name(),
            config.evaluation.judge_temperature,
        )

    # ------------------------------------------------------------------
    # Public API
    # ------------------------------------------------------------------

    def score(
        self,
        metric: MetricType,
        question: str,
        answer: str,
        contexts: list[str],
        ground_truth: str,
    ) -> JudgeResult:
        """Score a single answer on a single metric.

        Args:
            metric:       Which metric to evaluate.
            question:     The original question posed to the RAG pipeline.
            answer:       The generated answer from the RAG pipeline.
            contexts:     List of retrieved context chunk texts.
            ground_truth: The reference/expected answer.

        Returns:
            JudgeResult with score in [0.0, 1.0], justification, and raw LLMResponse.
        """
        logger.debug(
            "Judge.score: metric=%s question_len=%d answer_len=%d n_contexts=%d",
            metric.value, len(question), len(answer), len(contexts),
        )

        prompt = self._build_prompt(metric, question, answer, contexts, ground_truth)
        messages = [
            {"role": "system", "content": _SYSTEM_PROMPT},
            {"role": "user", "content": prompt},
        ]

        t_start = time.monotonic()
        try:
            response = self._provider.complete(
                messages=messages,
                temperature=self._config.evaluation.judge_temperature,
                max_tokens=512,   # judge output is always short (JSON + justification)
            )
        except Exception as exc:
            logger.error(
                "Judge.score: LLM call failed for metric=%s: %s", metric.value, exc
            )
            # Return a sentinel failure result without a real LLMResponse.
            # Callers must handle the case where score == 0.0 and justification
            # contains an error message.
            from src.llm.base import LLMResponse
            dummy_response = LLMResponse(
                text="",
                input_tokens=0,
                output_tokens=0,
                model=self._provider.get_model_name(),
                latency_ms=0.0,
                cost_usd=0.0,
            )
            return JudgeResult(
                metric=metric,
                score=0.0,
                justification=f"LLM call failed: {exc}",
                llm_response=dummy_response,
            )

        latency_ms = (time.monotonic() - t_start) * 1000

        logger.debug(
            "Judge.score: metric=%s latency=%.0fms input_tokens=%d output_tokens=%d cost=$%.6f",
            metric.value, latency_ms,
            response.input_tokens, response.output_tokens, response.cost_usd,
        )

        # Record telemetry
        operation = f"judge_{metric.value}"
        if self._telemetry is not None:
            self._provider._record_call(
                response=response,
                operation=operation,
                pipeline=None,
            )

        # Parse JSON response
        score, justification = self._parse_response(response.text, metric)

        logger.info(
            "Judge.score: metric=%s score=%.3f", metric.value, score
        )

        return JudgeResult(
            metric=metric,
            score=score,
            justification=justification,
            llm_response=response,
        )

    def score_all_metrics(
        self,
        question: str,
        answer: str,
        contexts: list[str],
        ground_truth: str,
    ) -> list[JudgeResult]:
        """Score all 4 metrics for a single question/answer/context set.

        Makes 4 sequential LLM calls (one per metric).

        Args:
            question:     Original question.
            answer:       Generated answer from RAG pipeline.
            contexts:     Retrieved context chunk texts.
            ground_truth: Reference answer.

        Returns:
            List of 4 JudgeResult objects, one per MetricType.
        """
        logger.info(
            "Judge.score_all_metrics: scoring %d metrics for question_len=%d",
            len(MetricType), len(question),
        )

        results: list[JudgeResult] = []
        for metric in MetricType:
            result = self.score(
                metric=metric,
                question=question,
                answer=answer,
                contexts=contexts,
                ground_truth=ground_truth,
            )
            results.append(result)

        scores_summary = {r.metric.value: round(r.score, 3) for r in results}
        logger.info("Judge.score_all_metrics: complete — scores=%s", scores_summary)

        return results

    # ------------------------------------------------------------------
    # Private helpers
    # ------------------------------------------------------------------

    def _build_prompt(
        self,
        metric: MetricType,
        question: str,
        answer: str,
        contexts: list[str],
        ground_truth: str,
    ) -> str:
        """Render the metric-specific prompt template.

        Args:
            metric:       MetricType to render template for.
            question:     Question text.
            answer:       Generated answer text.
            contexts:     List of retrieved chunk texts.
            ground_truth: Reference answer text.

        Returns:
            Rendered prompt string.
        """
        # Format contexts as numbered list for readability
        contexts_block = "\n\n".join(
            f"[Chunk {i + 1}]:\n{ctx.strip()}"
            for i, ctx in enumerate(contexts)
        )

        template = _METRIC_PROMPTS[metric]
        return template.format(
            question=question.strip(),
            answer=answer.strip(),
            contexts_block=contexts_block,
            ground_truth=ground_truth.strip(),
        )

    def _parse_response(
        self,
        response_text: str,
        metric: MetricType,
    ) -> tuple[float, str]:
        """Parse LLM JSON response into (score, justification).

        Handles accidental markdown fences. On any parse failure, logs a
        warning and returns (0.0, "<error description>").

        Args:
            response_text: Raw LLM output text.
            metric:        MetricType for log context.

        Returns:
            Tuple of (score: float in [0.0, 1.0], justification: str).
        """
        text = response_text.strip()

        # Strip accidental markdown code fences
        if text.startswith("```"):
            lines = text.splitlines()
            inner_lines: list[str] = []
            in_fence = False
            for line in lines:
                if line.startswith("```") and not in_fence:
                    in_fence = True
                    continue
                if line.startswith("```") and in_fence:
                    break
                inner_lines.append(line)
            text = "\n".join(inner_lines).strip()

        try:
            parsed: dict = json.loads(text)
        except json.JSONDecodeError as exc:
            logger.warning(
                "_parse_response: JSON parse failed for metric=%s: %s — returning score=0.0",
                metric.value, exc,
            )
            logger.debug("_parse_response: raw text: %s", response_text[:300])
            return 0.0, f"JSON parse error: {exc}"

        if not isinstance(parsed, dict):
            logger.warning(
                "_parse_response: expected dict, got %s for metric=%s — returning score=0.0",
                type(parsed).__name__, metric.value,
            )
            return 0.0, f"Unexpected response type: {type(parsed).__name__}"

        # Extract and validate score
        raw_score = parsed.get("score")
        if raw_score is None:
            logger.warning(
                "_parse_response: 'score' key missing for metric=%s — returning score=0.0",
                metric.value,
            )
            return 0.0, "Missing 'score' key in response"

        try:
            score = float(raw_score)
        except (TypeError, ValueError) as exc:
            logger.warning(
                "_parse_response: score=%r is not a number for metric=%s: %s — returning 0.0",
                raw_score, metric.value, exc,
            )
            return 0.0, f"Non-numeric score: {raw_score!r}"

        # Clamp to [0.0, 1.0]
        if not (0.0 <= score <= 1.0):
            logger.warning(
                "_parse_response: score=%.4f out of [0,1] for metric=%s — clamping",
                score, metric.value,
            )
            score = max(0.0, min(1.0, score))

        justification = str(parsed.get("justification", "")).strip()
        if not justification:
            logger.warning(
                "_parse_response: 'justification' empty for metric=%s", metric.value
            )
            justification = "No justification provided."

        return score, justification
